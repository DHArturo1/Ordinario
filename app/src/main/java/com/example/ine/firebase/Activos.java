package com.example.ine.firebase;

public class Activos {

    String Nombre;
    String Domicilio;
    String FechaNacimiento;
    String Genero;
    String ClaveElectoral;
    String Curp;
    String AnioRegistro;
    String NumeroVersion;
    String Distrito;
    String Seccion;
    String Localidad;
    String AnioEmision;
    String Vigencia;
    String Estatus;

    public Activos() {
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String nombre) {
        Nombre = nombre;
    }

    public String getDomicilio() {
        return Domicilio;
    }

    public void setDomicilio(String domicilio) {
        Domicilio = domicilio;
    }

    public String getFechaNacimiento() {
        return FechaNacimiento;
    }

    public void setFechaNacimiento(String fechaNacimiento) {
        FechaNacimiento = fechaNacimiento;
    }

    public String getGenero() {
        return Genero;
    }

    public void setGenero(String genero) {
        Genero = genero;
    }

    public String getClaveElectoral() {
        return ClaveElectoral;
    }

    public void setClaveElectoral(String claveElectoral) {
        ClaveElectoral = claveElectoral;
    }

    public String getCurp() {
        return Curp;
    }

    public void setCurp(String curp) {
        Curp = curp;
    }

    public String getAnioRegistro() {
        return AnioRegistro;
    }

    public void setAnioRegistro(String anioRegistro) {
        AnioRegistro = anioRegistro;
    }

    public String getNumeroVersion() {
        return NumeroVersion;
    }

    public void setNumeroVersion(String numeroVersion) {
        NumeroVersion = numeroVersion;
    }

    public String getDistrito() {
        return Distrito;
    }

    public void setDistrito(String distrito) {
        Distrito = distrito;
    }

    public String getSeccion() {
        return Seccion;
    }

    public void setSeccion(String seccion) {
        Seccion = seccion;
    }

    public String getLocalidad() {
        return Localidad;
    }

    public void setLocalidad(String localidad) {
        Localidad = localidad;
    }

    public String getAnioEmision() {
        return AnioEmision;
    }

    public void setAnioEmision(String anioEmision) {
        AnioEmision = anioEmision;
    }

    public String getVigencia() {
        return Vigencia;
    }

    public void setVigencia(String vigencia) {
        Vigencia = vigencia;
    }

    public String getEstatus() {
        return Estatus;
    }

    public void setEstatus(String estatus) {
        Estatus = estatus;
    }
}
